import numpy as np

# 定义矩阵 A (4x4)
A = np.array([
    [0.15, 0.65, 0.31, 0.13],
    [-0.23, 0.44, -0.19, 1.18],
    [1.39, -0.35, 0.88, 0.09],
    [0.20, 1.28, -1.29, -1.24]
])

# 定义矩阵 B (4x4)
B = np.array([
    [-0.53, 0.37, 0.78, 0.35],
    [1.11, 0.06, -0.13, 0.17],
    [1.31, -0.65, -0.17, -0.49],
    [-0.31, 0.80, 0.17, -0.07]
])

# 矩阵乘法
C = np.dot(A, B) # np.dot(A, B) 等价于 A @ B,计算 A 和 B 的矩阵乘积
print("结果矩阵 C (4x4):")
print(C)